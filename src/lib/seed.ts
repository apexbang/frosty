				objectives: ['hold the strongpoint'],
				manifest: {
					doctrine: 'positional-defense',
					echelon: 'platoon',
					organicAssets: ['small_arms', 'rpg', 'pkm'],
					supportingAssets: [],
					prohibited: ['heavy_weapons']
				},
